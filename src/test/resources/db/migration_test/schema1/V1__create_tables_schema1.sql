CREATE TABLE IF NOT EXISTS SCHEMA_1.USER_APP (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255),
    email VARCHAR(255)
    );

CREATE TABLE IF NOT EXISTS SCHEMA_1.CAR (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    entity VARCHAR(255),
    make_year INT,
    model VARCHAR2(255),
    user_id BIGINT,
    CONSTRAINT CAR_USER_FK FOREIGN KEY (user_id) REFERENCES SCHEMA_1.USER_APP(id)
    );